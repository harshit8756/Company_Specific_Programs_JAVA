
public class asciiCharacter {
    public static void main(String[] args) {
        char ch = 'a';
        char ch1 = 'b';

        int ascii1 = (int) ch;
        int ascii2 = (int) ch1;

        System.out.println("The ascii value of the character is :- " + ascii1);
        System.out.println("The ascii value of the character is :- " + ascii2);

    }
}
